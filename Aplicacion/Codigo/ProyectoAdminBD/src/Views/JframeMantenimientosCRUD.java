    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
        /**
         * ZONA DE COMBO BOX
         */
        try {
            /**
             * ZONA DE COMBO BOX
             */
            //Quitar la edicion para actualizar datos
            if (TablaMantenimientos.isEditing()) {
                TablaMantenimientos.getCellEditor().stopCellEditing();
            }
            if (TablaMantenimientos.getSelectedRow() == -1) {
                JOptionPane.showMessageDialog(rootPane, "Porfavor selecciona la fila que deseas actualizar");
            } else {
                Mantenimiento.setInd_mantenimiento(Integer.parseInt(String.valueOf(TablaMantenimientos.getModel().getValueAt(TablaMantenimientos.getSelectedRow(), 0))));
                Mantenimiento.setInd_transporte(combo.getItemAt(combo.getSelectedIndex()).getInd_transporte());
                Mantenimiento.setFec_entrada(Date.valueOf(String.valueOf(TablaMantenimientos.getModel().getValueAt(TablaMantenimientos.getSelectedRow(), 2))));
                Mantenimiento.setFec_salidad(Date.valueOf(String.valueOf(TablaMantenimientos.getModel().getValueAt(TablaMantenimientos.getSelectedRow(), 3))));
                Mantenimiento.setDes_mantenimiento(String.valueOf(TablaMantenimientos.getModel().getValueAt(TablaMantenimientos.getSelectedRow(), 4)));
                Mantenimiento.setCan_mantenimiento(Integer.parseInt(String.valueOf(TablaMantenimientos.getModel().getValueAt(TablaMantenimientos.getSelectedRow(), 5))));
                Mantenimiento.setVal_mantenimiento(Integer.parseInt(String.valueOf(TablaMantenimientos.getModel().getValueAt(TablaMantenimientos.getSelectedRow(), 6))));

                if (controlador.actualizar_mantenimientos(Mantenimiento)) {
                    JOptionPane.showMessageDialog(rootPane, "Actualizado Correctamente");
                } else {
                    JOptionPane.showMessageDialog(rootPane, "Ocurrio un error");
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, "Puede que hayas ingresado una fecha invalida, ha ocurrido el siguiente error \n" + e.getMessage());
        }
    }//GEN-LAST:event_btnActualizarActionPerformed

