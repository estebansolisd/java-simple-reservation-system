    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed

        //Quitar la edicion para actualizar datos
        if (TablaMantenimientos.isEditing()) {
            TablaMantenimientos.getCellEditor().stopCellEditing();
        }
        try {
            Mantenimiento.setInd_mantenimiento(Integer.parseInt(String.valueOf(TablaMantenimientos.getModel().getValueAt(TablaMantenimientos.getModel().getRowCount() - 1, 0))));
            Mantenimiento.setInd_transporte(combo.getItemAt(combo.getSelectedIndex()).getInd_transporte());
            Mantenimiento.setFec_entrada(Date.valueOf(String.valueOf(TablaMantenimientos.getModel().getValueAt(TablaMantenimientos.getModel().getRowCount() - 1, 2))));
            Mantenimiento.setFec_salidad(Date.valueOf(String.valueOf(TablaMantenimientos.getModel().getValueAt(TablaMantenimientos.getModel().getRowCount() - 1, 3))));
            Mantenimiento.setDes_mantenimiento(String.valueOf(TablaMantenimientos.getModel().getValueAt(TablaMantenimientos.getModel().getRowCount() - 1, 4)));
            Mantenimiento.setCan_mantenimiento(Integer.parseInt(String.valueOf(TablaMantenimientos.getModel().getValueAt(TablaMantenimientos.getModel().getRowCount() - 1, 5))));
            Mantenimiento.setVal_mantenimiento(Integer.parseInt(String.valueOf(TablaMantenimientos.getModel().getValueAt(TablaMantenimientos.getModel().getRowCount() - 1, 6))));
            if (controlador.insertar_mantenimientos(Mantenimiento)) {
                JOptionPane.showMessageDialog(rootPane, "Insertado Correctamente");
            } else {
                JOptionPane.showMessageDialog(rootPane, "Ocurrio un error");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, "Asegurate de haber llenado la fila");
        }

    }//GEN-LAST:event_btnGuardarActionPerformed

