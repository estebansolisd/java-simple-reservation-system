    public static DefaultTableModel ConstruirTabla(ResultSet rs)
            throws SQLException {

        ResultSetMetaData metaData = rs.getMetaData();

        // names of columns
        Vector<String> columnas = new Vector<String>();
        int columnCount = metaData.getColumnCount();
        columnas.add("N√∫mero de Identificaci√≥n de Mantenimiento");
        columnas.add("Tipo de transporte");
        columnas.add("Fecha de entrada");
        columnas.add("Fecha de Salida");
        columnas.add("Descripci√≥n de mantenimiento");
        columnas.add("Can. Mantenimiento");
        columnas.add("Val. Mantenimiento");
        /**
         * for (int column = 1; column <= columnCount; column++) {
         * columnas.add(metaData.getColumnName(column)); }
         */

        // data of the table
        Vector<Vector<Object>> data = new Vector<Vector<Object>>();
        while (rs.next()) {
            Vector<Object> vector = new Vector<Object>();
            for (int columnIndex = 1; columnIndex <= columnCount; columnIndex++) {
                vector.add(rs.getObject(columnIndex));
            }
            data.add(vector);
        }

        return new DefaultTableModel(data, columnas);

    }

