    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
        //Quitar la edicion para actualizar datos
        if (TablaEmpleados.isEditing()) {
            TablaEmpleados.getCellEditor().stopCellEditing();
        }
        if (TablaEmpleados.getSelectedRow() == -1) {
            JOptionPane.showMessageDialog(rootPane, "Porfavor selecciona la fila que deseas actualizar");
        } else {
            //JOptionPane.showMessageDialog(rootPane, Integer.parseInt(String.valueOf(TablaEmpleados.getValueAt(TablaEmpleados.getSelectedRow(), 0))));
            Empleado.setInd_empleado(Integer.parseInt(String.valueOf(TablaEmpleados.getModel().getValueAt(TablaEmpleados.getSelectedRow(), 0))));
            Empleado.setNum_cedula(String.valueOf(TablaEmpleados.getModel().getValueAt(TablaEmpleados.getSelectedRow(), 1)));
            Empleado.setDes_nombre(String.valueOf(TablaEmpleados.getModel().getValueAt(TablaEmpleados.getSelectedRow(), 2)));
            Empleado.setTip_cargo(String.valueOf(TablaEmpleados.getModel().getValueAt(TablaEmpleados.getSelectedRow(), 3)));
            Empleado.setDes_direccion(String.valueOf(TablaEmpleados.getModel().getValueAt(TablaEmpleados.getSelectedRow(), 4)));
            Empleado.setNom_pais(String.valueOf(TablaEmpleados.getModel().getValueAt(TablaEmpleados.getSelectedRow(), 5)));
            Empleado.setNom_distrito(String.valueOf(TablaEmpleados.getModel().getValueAt(TablaEmpleados.getSelectedRow(), 6)));
            Empleado.setNom_canton(String.valueOf(TablaEmpleados.getModel().getValueAt(TablaEmpleados.getSelectedRow(), 7)));
            Empleado.setNum_telefono(String.valueOf(TablaEmpleados.getModel().getValueAt(TablaEmpleados.getSelectedRow(), 8)));
            Empleado.setDes_email(String.valueOf(TablaEmpleados.getModel().getValueAt(TablaEmpleados.getSelectedRow(), 9)));
            if (controlador.actualizar_empleados(Empleado)) {
                JOptionPane.showMessageDialog(rootPane, "Actualizado Correctamente");
            } else {
                JOptionPane.showMessageDialog(rootPane, "Ocurrio un error");
            }
        }
    }//GEN-LAST:event_btnActualizarActionPerformed

